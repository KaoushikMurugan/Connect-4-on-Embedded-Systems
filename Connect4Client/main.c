void drawBoard(unsigned char grid[6][7]) {
    int offset = GRID_UNIT;

    int row, col;
    for (row = 0; row <= ROWS; row++) {
        drawLine(0, GRID_UNIT*row + offset, COLS*GRID_UNIT, GRID_UNIT*row + offset, WHITE);
    }

    for (col = 0; col <= COLS; col++) {
        drawLine(GRID_UNIT*col, offset, GRID_UNIT*col, ROWS*GRID_UNIT + offset, WHITE);
    }

    for (row = 0; row < ROWS; row++) {
        for (col = 0; col < COLS; col++) {
            switch(grid[row][col]) {
            case 1:
                fillCircle(GRID_UNIT*col + COIN_RADIUS + 1, GRID_UNIT*row + COIN_RADIUS + 1 + offset, COIN_RADIUS-1, RED);
                break;
            case 2:
                fillCircle(GRID_UNIT*col + COIN_RADIUS + 1, GRID_UNIT*row + COIN_RADIUS + 1 + offset, COIN_RADIUS-1, BLUE);
                break;
            }
        }
    }
}

