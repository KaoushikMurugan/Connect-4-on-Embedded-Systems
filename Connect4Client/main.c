/**
 * Reset SysTick Counter
 */
static inline void SysTickReset(void) {
    // any write to the ST_CURRENT register clears it
    // after clearing it automatically gets reset without
    // triggering exception logic
    // see reference manual section 3.2.1
    HWREG(NVIC_ST_CURRENT) = 1;

    // clear the global count variable
    systick_cnt = 0;
}

/**
 * SysTick Interrupt Handler
 *
 * Keep track of whether the systick counter wrapped
 */
static void SysTickHandler(void) {
    // increment every time the systick handler fires
    systick_cnt++;
}

/**
 * Initializes SysTick Module
 */
static void SysTickInit(void) {

    // configure the reset value for the systick countdown register
    MAP_SysTickPeriodSet(SYSTICK_RELOAD_VAL);

    // register interrupts on the systick module
    MAP_SysTickIntRegister(SysTickHandler);

    // enable interrupts on systick
    // (trigger SysTickHandler when countdown reaches 0)
    MAP_SysTickIntEnable();

    // enable the systick module itself
    MAP_SysTickEnable();
}

// chech that a number (test) is within tolerance of value
char withinTolerance(const int test, const int value)
{
    return ((value - tolerance) <= test) && (test <= (value + tolerance));
}

//*****************************************************************************
//
//! IR Sensor Falling Edge Interrupt Handler
//!
//! \param  None
//!
//! \return None
//!
//*****************************************************************************
static void IRIntHandler(void) {
    unsigned long ulStatus = MAP_GPIOIntStatus (GPIOA0_BASE, true); // get interrupt status
    MAP_GPIOIntClear(GPIOA0_BASE, ulStatus);        // clear interrupts on GPIOA1

    // read the countdown register and compute elapsed cycles
    uint64_t delta = SYSTICK_RELOAD_VAL - SysTickValueGet();

    // convert elapsed cycles to microseconds
    uint64_t delta_us = TICKS_TO_US(delta);

    // if signal hasn't been received, process current signal
    if (!signalReceived) {
        // check that pulse is within valid tolerance
        if(withinTolerance(delta_us, startPulse))
        {
            // start pulse so process new bit string
            curBitString = 0;
            bitsSoFar = 0;
        }
        else if(withinTolerance(delta_us, shortPulse))
        {
            // short pulse so add a 1 to bit string
            curBitString = (curBitString << 1);
            bitsSoFar++;
        }
        else if(withinTolerance(delta_us, longPulse))
        {
            // long pulse so add a 0 to bit string
            curBitString = (curBitString << 1) + 1;
            bitsSoFar++;
        }
        else {
            // invalid pulse so reset bit string
            curBitString = 0;
            bitsSoFar = 0;
        }

        // if the # bits is exactly 32, then signal is valid
        if(bitsSoFar == 32)
        {
            // cache the bit string
            cachedBitString = curBitString;

            // change flag that signal is received
            signalReceived = 1;
        }
    }

    // reset systick timer
    SysTickReset();
}

